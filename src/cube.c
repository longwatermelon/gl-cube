struct Cube *cube_alloc(vec3 pos, vec3 rot)
{
    struct Cube *c = malloc(sizeof(struct Cube));
    glm_vec3_dup(pos, c->pos);
    glm_vec3_dup(rot, c->rot);

    glGenVertexArrays(1, &c->vao);
    glBindVertexArray(c->vao);

    glGenBuffers(1, &c->vb);
    glBindBuffer(GL_ARRAY_BUFFER, c->vb);
    glBufferData(GL_ARRAY_BUFFER, sizeof(g_verts), g_verts, GL_STATIC_DRAW);

    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);

    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);

    glm_mat4_identity(c->model);
//    glm_translate(c->model, c->pos);

    return c;
}


void cube_free(struct Cube *c)
{
    free(c);
}


void cube_render(struct Cube *c, RenderInfo *ri)
{
    glUseProgram(ri->shader);

    shader_mat4(ri->shader, "model", c->model);
    shader_mat4(ri->shader, "view", ri->view);
    shader_mat4(ri->shader, "projection", ri->proj);

    glBindVertexArray(c->vao);
    glDrawArrays(GL_TRIANGLES, 0, 36);
}

